import numpy as np

def random_predict(number:int=1) -> int:
    """–андомно угадываем число

    Args:
        number (int, optional): «агаданное число. Defaults to 1.

    Returns:
        int: „исло попыток
    """
    
    count = 0

    while True:
        count += 1
        predict_number = np.random.randint(1, 101) # предполагаемое число
        if number == predict_number:
            break # выход из цикла, если угадали
    return(count)

def score_game(random_predict) -> int:
    """«а какое количество попыток в среднем из 1000 подходов угадывает наш алгоритм

    Args:
        random_predict ([type]): функци€ угадывани€

    Returns:
        int: среднее количество попыток
    """

    count_ls = [] # список дл€ сохранени€ количества попыток
    np.random.seed(1) # фиксируем сид дл€ воспроизводимости
    random_array = np.random.randint(1, 101, size=(1000)) # загадали список чисел

    for number in random_array:
        count_ls.append(random_predict(number))

    score = int(np.mean(count_ls)) # находим среднее количество попыток

    print(f'¬аш алгоритм угадывает число в среднем за: {score} попыток')
    return(score)

# RUN
if __name__ == '__main__':
    score_game(random_predict)
